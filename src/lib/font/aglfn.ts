/** Return the AGLFN name for a codepoint, or a derived name as a fallback. */
export const aglfnName = (codepoint: number): string => {
	if (AGLFN[codepoint]) return AGLFN[codepoint];
	// Basic Latin letters and digits use their character directly
	if (
		(codepoint >= 0x0030 && codepoint <= 0x0039) ||
		(codepoint >= 0x0041 && codepoint <= 0x005a) ||
		(codepoint >= 0x0061 && codepoint <= 0x007a)
	) {
		return String.fromCodePoint(codepoint);
	}
	// Latin Extended A/B with accents — Aacute, eacute, etc.
	const acc = extendedLatin(codepoint);
	if (acc) return acc;
	// Combining marks: uniXXXX is fine; AGL also accepts "acutecmb" etc.
	return `uni${codepoint.toString(16).toUpperCase().padStart(4, '0')}`;
};

